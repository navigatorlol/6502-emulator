#!/bin/python

rom=bytearray([0xEA]*65536)
rom[0x8000]=0x38  # SEC
rom[0x8001]=0xA9  # LDA 
rom[0x8002]=0x80
rom[0x8003]=0xE9  # SBC
rom[0x8004]=0x01
rom[0x8005]=0x8D  # STA
rom[0x8006]=0x32
rom[0x8007]=0x32
rom[0x8008]=0xEA  # NOP
rom[0x8009]=0xA9  # LDA
rom[0x800A]=0x05
rom[0x800B]=0x69  # ADC
rom[0x800C]=0x05
rom[0xFFFC]=0x4C  # JMP
rom[0xFFFD]=0x00
rom[0xFFFE]=0x80

with open("rom.bin", "wb") as out_file:
    out_file.write(rom)

